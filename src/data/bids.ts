// realistic spread so list-view aggregates aren't all zero.

type ExitRowSeed = {
  id: number;
  passengerId: Passenger["id"];
  bid: number;
  channel: Channel;
  time: string;
};

function exitRowBids(flightId: Flight["id"], rows: ExitRowSeed[]): Bid[] {
  return rows.map((r) => ({
    flightId,
    id: r.id,
    product: "exitRows",
    passengerId: r.passengerId,
    bid: r.bid,
    mult: 1.0,
    channel: r.channel,
    time: r.time,
    state: "pending",
  }));
}

